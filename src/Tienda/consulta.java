    private void consultarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_consultarMouseClicked
        // TODO add your handling code here:

        String cod = consulta.getText();
        try {
            Connection c = conexion.conexion();
            Statement stmt = c.createStatement();
            ResultSet res = stmt.executeQuery("select * from productos where codigo='" + cod + "';");
            System.out.println("res"+ res);
            while (res.next()) {
                String id = res.getString("codigo");
                String nom = res.getString("nombre");
                String mar = res.getString("marca");
                String exi = res.getString("existencia");
                String preIva = res.getString("precioIva");
                String pre = res.getString("precioSinIva");
                codigo.setText(id);
                nombre.setText(nom);
                marca.setText(mar);
                existencia.setText(exi);
                precioIva.setText(preIva);
                precio.setText(pre);
            }
            modificar.setEnabled(true);
            stmt.close();
            c.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de conexion" + e);
        }
    }//GEN-LAST:event_consultarMouseClicked

