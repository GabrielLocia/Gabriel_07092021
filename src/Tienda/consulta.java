    private void modificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_modificarMouseClicked
        // TODO add your handling code here:
        String id = codigo.getText();
        String nom = nombre.getText();
        String mar = marca.getText();
        String exi = existencia.getText();
        float preIva = Float.valueOf(precioIva.getText());
        float preciof = (float) (preIva / 1.16);

        String preIvas = String.valueOf(preIva);
        String precios = String.valueOf(preciof);

        try {
            Connection c = conexion.conexion();

            Statement stmt = c.createStatement();   //--
            int i = stmt.executeUpdate("UPDATE  productos set codigo='" + id + "',nombre='" + nom + "',marca='" + mar + "',existencia='" + exi + "',precioIva='" + preIvas + "',precioSinIva='" + precios +"'WHERE codigo='"+id+"';");
            System.out.println("i:"+i);
            if (i > 0) {
                JOptionPane.showMessageDialog(null, "Producto actiañlizado....");
            }else{
                 JOptionPane.showMessageDialog(null, "codigo del producto no existe....");
            }
            
            
            precio.setText(precios);
            stmt.close();
            c.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en ingreso" + e);
        }


    }//GEN-LAST:event_modificarMouseClicked

