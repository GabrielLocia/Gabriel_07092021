    private void eliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_eliminarMouseClicked
        // TODO add your handling code here:
        int de = JOptionPane.showConfirmDialog(null, "Â¿Estas seguro de eliminar este producto?");
        String cod = codigo.getText();

        if (de == JOptionPane.YES_OPTION) {
            try {
                Connection c = conexion.conexion();

                Statement stmt = c.createStatement();   //--
                int i = stmt.executeUpdate("DELETE FROM productos  WHERE codigo= '" + cod + "';");
                if (i > 0) {
                    JOptionPane.showMessageDialog(null, "Producto eliminado....");
                }
                codigo.setText("");
               
                stmt.close();
                c.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error en ingreso" + e);
            }
        }
        
    }//GEN-LAST:event_eliminarMouseClicked

